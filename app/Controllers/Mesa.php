                            $errors = $validation -> getErrors();
                            $data = array(
                                "Status" => 404,
                                "Detalles" => $errors
                            );
                            return json_encode($data, true);
                        }else{
                            $model = new MesaModel();
                            $mesa = $model -> find($id);
                            if(is_null($mesa)){
                                $data = array(
                                    "Status" => 404,
