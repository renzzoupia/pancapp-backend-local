    public function create(){
        $request = \Config\Services::request();
        $validation = \Config\Services::validation();
        $headers = $request -> getHeaders();
        $model = new UsuarioModel();
        $registro = $model->where('usua_estado', 1) -> findAll();
        //var_dump($registro); die;
        foreach($registro as $key => $value){
            if(array_key_exists('Authorization',$headers)&& !empty($headers['Authorization'])){
                if($request -> getHeader('Authorization') == 'Authorization: Basic '
                .base64_encode($value['usua_user_secreto'].':'.$value['usua_llave_secreta'])){
                        $datos = array(
                            "mesa_rest_id" => $request -> getVar("mesa_rest_id"),
                            "mesa_numero" => $request -> getVar("mesa_numero"),
                            "mesa_cantidad_personas" => $request -> getVar("mesa_cantidad_personas"),
                            "mesa_referencia_ubicacion" => $request -> getVar("mesa_referencia_ubicacion"),
                            "mesa_activo" => $request -> getVar("mesa_activo")
                        );
                        if(!empty($datos)){
                            $validation -> setRules([
                                "mesa_rest_id" => 'required|integer',
                                "mesa_numero" => 'required|string|max_length[30]',
                                "mesa_cantidad_personas" => 'required|integer',
                                "mesa_referencia_ubicacion" => 'required|string|max_length[100]',
                                "mesa_activo" => 'required|integer'
                            ]);
                            $validation -> withRequest($this -> request) -> run();
                            if($validation -> getErrors()){
                                $errors = $validation -> getErrors();
                                $data = array(
                                    "Status" => 404,
                                    "Detalles" => $errors
                                );
                                return json_encode($data, true);
                            }else{
                                $datos = array(
                                    "mesa_rest_id" => $datos["mesa_rest_id"],
                                    "mesa_numero" => $datos["mesa_numero"],
                                    "mesa_cantidad_personas" => $datos["mesa_cantidad_personas"],
                                    "mesa_referencia_ubicacion" => $datos["mesa_referencia_ubicacion"],
                                    "mesa_activo" => $datos["mesa_activo"]
                                );
                                $model = new MesaModel();
                                $mesa_id = $model -> insert($datos);
                                $data = array(
                                    "Status" => 200,
                                    "Detalles" => "Registro existoso",
                                    "mesa_id" => $mesa_id
                                );
                                return json_encode($data, true);
                            }
                        }else{
                            $data = array(
                                "Status" => 404,
                                "Detalles" => "Registro con errores"
                            );
                            return json_encode($data, true);
                        }
                }else{
                    $data = array(
                        "Status" => 404,
                        "Detalles" => "El token es incorrecto"
                    );
                }
            }else{
                $data = array(
                    "Status" => 404,
                    "Detalles" => "No posee autorización"
                );
            }
        }
        return json_encode($data, true);
    }

    public function update($id){
        $request = \Config\Services::request();
        $validation = \Config\Services::validation();
        $headers = $request -> getHeaders();
        $model = new UsuarioModel();
        $registro = $model->where('usua_estado', 1) -> findAll();
        foreach($registro as $key => $value){
            if(array_key_exists('Authorization',$headers)&& !empty($headers['Authorization'])){
                if($request -> getHeader('Authorization') == 'Authorization: Basic '
                .base64_encode($value['usua_user_secreto'].':'.$value['usua_llave_secreta'])){
                    $datos = $this -> request -> getRawInput();
                    if(!empty($datos)){
                        $validation -> setRules([
                            "mesa_rest_id" => 'required|integer',
                            "mesa_numero" => 'required|string|max_length[30]',
                            "mesa_cantidad_personas" => 'required|integer',
                            "mesa_referencia_ubicacion" => 'required|string|max_length[100]',
                            "mesa_activo" => 'required|integer'
                        ]);
